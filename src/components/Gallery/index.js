class Gallery extends Component {
  state = {activeImage: imagesList[0].id}

  getGaleryImage = () => {
    const {activeImage} = this.state
    const filterImage = imagesList.filter(
      eachImage => eachImage.id === activeImage,
    )
    return filterImage
  }

  thumbnailImage = id => {
    this.setState({activeImage: id})
  }

  render() {
    const filterImage = this.getGaleryImage()
    const {activeImage} = this.state
    return (
      <div className="gallery-main-container">
        <div className="gallery-container">
          <ul>
            {filterImage.map(each => (
              <li className="gallery-image">
                {' '}
                <img
                  src={each.imageUrl}
                  className="gallery-image"
                  alt={each.imageAltText}
                />{' '}
              </li>
            ))}
          </ul>
          <p className="gallery-para"> Nature Photography </p>
          <p className="para"> Nature Photography bt Filtered </p>

          <ul className="thumbnail-container">
            {imagesList.map(eachThumbnail => (
              <ThumbnailItem
                thumbnail={eachThumbnail}
                key={eachThumbnail.id}
                thumbnailImage={this.thumbnailImage}
                isActive={eachThumbnail.id === activeImage}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}
export default Gallery
